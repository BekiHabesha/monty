#include "monty.h"

/**
 *f_rotr- It rotates the stack to the bottom
 *
 * Author: Bereket Dereje Mekonnen
 *
 * @head: The stack head
 * @counter: The line_number
 *
 * Return: no return
 */

void f_rotr(stack_t **head, __attribute__((unused)) unsigned int counter)
{
	stack_t *copy;

	copy = *head;
	if (*head == NULL || (*head)->next == NULL)
	{
		return;
	}
	while (copy->next)
	{
		copy = copy->next;
	}
	copy->next = *head;
	copy->prev->next = NULL;
	copy->prev = NULL;
	(*head)->prev = copy;
	(*head) = copy;
}
